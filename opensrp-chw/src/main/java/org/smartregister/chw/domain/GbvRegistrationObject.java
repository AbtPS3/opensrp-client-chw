package org.smartregister.chw.domain;

public class GbvRegistrationObject {
    private String physicalViolence;
    private String emotionalViolence;
    private String sexualViolence;
    private String exploitationViolence;

    public String getPhysicalViolence() {
        return physicalViolence;
    }

    public void setPhysicalViolence(String physicalViolence) {
        this.physicalViolence = physicalViolence;
    }

    public String getEmotionalViolence() {
        return emotionalViolence;
    }

    public void setEmotionalViolence(String emotionalViolence) {
        this.emotionalViolence = emotionalViolence;
    }

    public String getSexualViolence() {
        return sexualViolence;
    }

    public void setSexualViolence(String sexualViolence) {
        this.sexualViolence = sexualViolence;
    }

    public String getExploitationViolence() {
        return exploitationViolence;
    }

    public void setExploitationViolence(String exploitationViolence) {
        this.exploitationViolence = exploitationViolence;
    }
}
